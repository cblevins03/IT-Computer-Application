/*Create the Computer ID table*/
CREATE TABLE computers (
    compid varchar(30) NOT NULL,
    compname varchar(30) NOT NULL,
    compos varchar(30) NOT NULL,
    compip varchar(20) NOT NULL,
    compsernum varchar(25) NOT NULL,
    compdatepurch varchar(25) NOT NULL,
    compstatus varchar(30) NOT NULL,
    compnotes varchar(300) NOT NULL,
);
